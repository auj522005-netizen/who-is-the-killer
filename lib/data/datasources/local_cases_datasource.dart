/// LocalCasesDatasource — In-memory database of all murder cases
///
/// Contains the fully expanded case data for Cases 1, 2, 3, and 7
/// as specified in the game design document. Each case includes
/// character profiles with secret roles, progressive clues, and
/// the verdict text.
///
/// This datasource is the single source of truth for case content
/// and can be replaced with a remote API when available.
/// ────────────────────────────────────────────────────────────────
class LocalCasesDatasource {
  /// In-memory cache of all cases
  static final List<Map<String, dynamic>> _casesData = [
    _case1BlindHatred,
    _case2PeakMatch,
    _case3TheInheritance,
    _case7UnderTheNails,
  ];

  /// Retrieves all available cases
  List<Map<String, dynamic>> getAllCases() {
    return List.unmodifiable(_casesData);
  }

  /// Retrieves a specific case by ID
  Map<String, dynamic>? getCaseById(String caseId) {
    try {
      return _casesData.firstWhere(
        (caseData) => caseData['id'] as String == caseId,
      );
    } catch (_) {
      return null;
    }
  }

  /// Retrieves all players for a specific case
  List<Map<String, dynamic>> getPlayersForCase(String caseId) {
    final caseData = getCaseById(caseId);
    if (caseData == null) return [];
    return List<Map<String, dynamic>>.from(caseData['players'] as List);
  }

  /// Retrieves the clue for a specific round of a case
  /// Returns EXACTLY ONE clue per round (strict enforcement)
  Map<String, dynamic>? getClueForRound(String caseId, int roundNumber) {
    final caseData = getCaseById(caseId);
    if (caseData == null) return null;
    final clues = List<Map<String, dynamic>>.from(caseData['clues'] as List);
    try {
      return clues.firstWhere(
        (clue) => clue['round_number'] as int == roundNumber,
      );
    } catch (_) {
      return null;
    }
  }

  /// Retrieves all clues for a case (ordered by round)
  List<Map<String, dynamic>> getAllCluesForCase(String caseId) {
    final caseData = getCaseById(caseId);
    if (caseData == null) return [];
    return List<Map<String, dynamic>>.from(caseData['clues'] as List);
  }

